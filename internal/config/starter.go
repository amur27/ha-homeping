// Конфигурация-заготовка для первого запуска.
// Логика модуля: при старте без файла конфигурации трей-режим не падает,
// а создаёт минимальный валидный конфиг-пример (docs/spec.md, раздел 2) —
// пользователь правит его через веб-интерфейс настроек. Токена заготовка
// не касается, поэтому агент стартует со статусом «не настроен».
package config

// Starter возвращает минимальную валидную конфигурацию-пример.
func Starter() *Config {
	return &Config{
		HomeAssistant: HomeAssistant{
			URL: "ws://homeassistant.local:8123/api/websocket",
		},
		Entities: []Entity{
			{
				ID:   "binary_sensor.front_door",
				Name: "Входная дверь (пример — замените на свой датчик)",
				States: map[string]string{
					"on":  "🚪 Дверь открыта",
					"off": "Дверь закрыта",
				},
			},
		},
	}
}
